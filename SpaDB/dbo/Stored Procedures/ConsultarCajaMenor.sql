CREATE PROCEDURE ConsultarCajaMenor(@IdEmpresa VARCHAR(36))
 AS
  BEGIN

	SELECT 
        ID_REGISTRO,AÑO,MES,DIA,QUINCENA, SALDO_INICIAL, ACUMULADO, FECHA_REGISTRO, FECHA_MODIFICACION, CAST(ID_EMPRESA AS VARCHAR(36)) AS ID_EMPRESA	    
	FROM CAJA_MENOR 
    WHERE CAST(ID_EMPRESA AS VARCHAR(36)) = @IdEmpresa

 END

 GO

 